   private void selectData() {
        String kolom[] = {"Absen","nama","tanggal","uang_masuk","uang_keluar","saldo"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM pemasukan";
        ResultSet rs = koneksiDB.executeQuery(SQL);
        
        try
        {
            rs = koneksiDB.executeQuery(SQL);
            while(rs.next())
            {
                String Absen = rs.getString(2);
                String nama = rs.getString(3);
                String tanggal = rs.getString(4);
                String uang_masuk = rs.getString(5);
                String uang_keluar = rs.getString(6);
                String saldo = rs.getString(7);
               
                                
                String data[] = {Absen,nama,tanggal,uang_masuk,uang_keluar,saldo};
                dtm.addRow(data);
            }
        }
        catch(SQLException ex)
        {
            Logger.getLogger(home.class.getName()).log(Level.SEVERE, null, ex);
        }
        T1.setModel(dtm); //To change body of generated methods, choose Tools | Templates.
    }

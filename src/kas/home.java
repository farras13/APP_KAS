    private void HapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusActionPerformed
        // TODO add your handling code here:
        int baris = T1.getSelectedRow();
        if(baris != -1){
            String Id = T1.getValueAt(baris, 0).toString();
            String SQL = "DELETE FROM pemasukan WHERE Absen ='"+Id+"'";
            int status = koneksiDB.execute(SQL);
            if (status == 1){
                JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus","SUSES",JOptionPane.INFORMATION_MESSAGE);
                selectData();
            }else{
                JOptionPane.showMessageDialog(this, "Data GAGAL Dihapus","GAGAL",JOptionPane.WARNING_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Pilih Baris Terlebih Dahulu","ERROR",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_HapusActionPerformed

    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        if(jns.equals("Penjumlahan")){
            select();
            }else{
            slec();
        }

        if ("".equals(Absen.getText()) || "".equals(nama.getText()) || "".equals(tgl.getDate())) {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data","ERROR", JOptionPane.WARNING_MESSAGE);
        } else {
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String kalender = dateFormat.format(tgl.getDate());
            String SQL = "INSERT INTO pemasukan (Absen,nama,tanggal,uang_masuk,uang_keluar,saldo) " + "VALUES('"+Absen.getText()+
                    "','"+nama.getText()+"','"+kalender+"','"+um.getText()+"','"+uk.getText()+"','"+sld.getText()+"')";
            int status = koneksiDB.execute(SQL);
            if (status == 1) {
                JOptionPane.showMessageDialog(this,"Data berhasil ditambahkan","Sukses",JOptionPane.INFORMATION_MESSAGE);
                selectData();
                Absen.setText("");
                nama.setText("");
                um.setText("0");
                uk.setText("0");
            }
            else{
                JOptionPane.showMessageDialog(this,"Data gagal ditambahkan","Gagal",JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_simpanActionPerformed

